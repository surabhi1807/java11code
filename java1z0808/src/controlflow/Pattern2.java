package controlflow;

public class Pattern2 {
	public static void main(String[] args) {
		
		for(int i=1; i<=4;i++)
	{
		for(int j=1; j<=i;j++)
		{
			System.out.print(j);
	}
	System.out.println();
	
     
}
 
	}
	public static void Pattern3() {
	for(int i=1;i<=5;i++) {
		for(int j=1;j>=i;j--) {
			System.out.print(j);
		}
		System.out.println();
		
	}
	
	}
}
	
		
		
	


